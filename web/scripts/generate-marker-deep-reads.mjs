#!/usr/bin/env node
/**
 * Writes web/lib/markerDeepReads.generated.ts from historic-markers-curated.json
 * and articles_information/markers/*.md (frontmatter slug for href).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import matter from 'gray-matter';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '../..');
const CURATED = path.join(__dirname, '../data/historic-markers-curated.json');
const MARKERS_DIR = path.join(REPO_ROOT, 'articles_information', 'markers');
const OUT = path.join(__dirname, '../lib/markerDeepReads.generated.ts');

function findArticleFile(markerSlug) {
  const candidates = [markerSlug, markerSlug.replace(/-\d+$/, '')];
  for (const c of candidates) {
    const fp = path.join(MARKERS_DIR, `${c}.md`);
    if (fs.existsSync(fp)) return fp;
  }
  return null;
}

function main() {
  const curated = JSON.parse(fs.readFileSync(CURATED, 'utf8'));
  const entries = [];

  for (const m of curated) {
    const fp = findArticleFile(m.slug);
    if (!fp) continue;
    const raw = matter(fs.readFileSync(fp, 'utf8'));
    const slug = String(raw.data.slug || path.basename(fp, '.md'));
    const href = `/information/${slug}/`;
    const title = `${String(raw.data.title || m.title).replace(/\s+/g, ' ').trim()} — full narrative`;
    let description = String(
      raw.data.excerpt ||
        raw.data.meta_description ||
        `Companion deep read for the historic marker “${m.title}”.`,
    )
      .replace(/\s+/g, ' ')
      .trim();
    if (description.length > 220) description = description.slice(0, 217) + '…';
    entries.push({ key: m.slug, href, title, description });
  }

  entries.sort((a, b) => a.key.localeCompare(b.key, 'en'));

  const lines = [
    '/** Auto-generated by web/scripts/generate-marker-deep-reads.mjs — do not edit by hand. */',
    '',
    "import type { MarkerDeepRead } from './markerDeepReadsTypes';",
    '',
    'export const MARKER_DEEP_READS_AUTO: Record<string, MarkerDeepRead> = {',
  ];

  for (const e of entries) {
    const keyLit = JSON.stringify(e.key);
    lines.push(`  ${keyLit}: {`);
    lines.push(`    href: ${JSON.stringify(e.href)},`);
    lines.push(`    title: ${JSON.stringify(e.title)},`);
    lines.push(`    description: ${JSON.stringify(e.description)},`);
    lines.push('  },');
  }

  lines.push('};');
  lines.push('');

  fs.writeFileSync(OUT, lines.join('\n'), 'utf8');
  console.log(`Wrote ${OUT} (${entries.length} marker → article links)`);
}

main();
