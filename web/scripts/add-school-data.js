function buildSchoolData() {
  const dataPath = path.resolve(__dirname, '..', 'data', 'town-data.json');
  const townData = JSON.parse(fs.readFileSync(dataPath, 'utf8'));

  let matched = 0;
  for (const [slug, school] of Object.entries(SCHOOL_DATA)) {
    if (townData[slug]) {
      townData[slug].schoolDistrict = school.district;
      townData[slug].schoolEnrollment = school.enrollment;
      townData[slug].schoolWebsite = school.website;
      matched++;
    }
  }

  fs.writeFileSync(dataPath, JSON.stringify(townData, null, 2));
  console.log(`School data added for ${matched} of ${Object.keys(townData).length} towns.`);
}

buildSchoolData();
